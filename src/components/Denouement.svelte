<p>
  The quote at the start of this article comes from an article titled
  "American Negro Exhibit" at the Paris Exposition, published on 3rd November 1900 in
  <a href="https://www.loc.gov/item/sn83027091/" target="_blank">The Colored American</a>, 
  an African American-led weekly publication based 
  in Washinton and founded in 1893. This, and other copies of The Colored American, are also 
  held by the Library of Congress and can be viewed online 
  <a href="https://chroniclingamerica.loc.gov/lccn/sn83027091/issues/first_pages/" target="_blank">here</a>. </p>
<p>
  This article was created using Svelte and D3, as part of the
   <a href="https://www.datavisualizationsociety.org/news/2024/2/2/advance-your-data-viz-skills-with-the-weekly-2024-du-bois-visualization-challenge" target="_blank">Data Visualisation Society 2024 Du Bois Challenge</a>, 
   a ten-week challenge that aims to celebrate W.E.B. Du Bois' legacy 
   by using modern data visualisation tools to recreate a selection of 
   the charts he presented at the 1900 Paris Exhibition. The source code can be viewed on GitHub 
   <a href="https://github.com/MarkHanly/dubois-challenge-2024" target="_blank">here</a>. 
   The code uses a <a href="https://github.com/connorrothschild/svelte-visualization-template" target="_blank">starter template</a> 
   provided by Connor Rothschild, and the scrollytelling style closely follows the example from his online course 
   <a href="https://www.newline.co/courses/better-data-visualizations-with-svelte/welcome" target="_blank">Better Data Visualizations with Svelte</a> 
   The background image for the chart was taken from <a href="https://github.com/Giammaria/Du-Bois-DVS-challenge" target="_blank">Madison Giammaria</a>.
